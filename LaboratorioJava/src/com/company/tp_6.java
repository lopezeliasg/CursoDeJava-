package com.company;

import static java.lang.Math.*;

public class tp_6 {
    public static void main(String[] args){
        //Funciones Trignometricas
        System.out.println("Funciones Trignometricas:");
        System.out.println();
        System.out.println("Seno de 90: "+Math.sin(90));
        System.out.println("Coseno de 45: "+Math.cos(45));
        System.out.println("Tangente de: "+Math.tan(30));
        System.out.println("Arcotangente de 77: "+Math.atan(77));
        System.out.println("Arcotangente del cociente de los argumentos 81 y 65: "+Math.atan2(81, 65));
        System.out.println();
        //Funcion exponencial y su inversa
        System.out.println("Funcion exponencial y su inversa:");
        System.out.println("Funcion exponencial de 101: "+Math.exp(101));
        System.out.println("Inversa de 101: "+Math.log(101));
        System.out.println();
        //constante pi & e
        System.out.println("Constante PI & E:");
        System.out.println("Constante de PI: "+Math.PI);
        System.out.println("Constante de E: "+Math.E);
    }
}
